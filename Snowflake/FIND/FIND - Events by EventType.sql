USE DATABASE CPE_DEV;
USE SCHEMA STAGING;

SELECT *
FROM
    CPE_DEV.STAGING.STAGE_EVENTS
WHERE
    INGESTED_TIMESTAMP > '2025-12-01 00:00:00.000'
    AND data:eventId::VARCHAR IN (
        'Test-Compress-Off-6-20251201-213154-0',
        'Test-Compress-On-6-20251201-213154-0'
    )
    ORDER BY INGESTED_TIMESTAMP DESC
LIMIT 100;